void set_icon(SDL_Window *window)
{
	SDL_Surface *surf;

	SDL_ClearError();

	surf = SDL_CreateRGBSurfaceFrom((void *)pixels, ICON_SIZE, ICON_SIZE, ICON_SIZE, ICON_SIZE * 2, 0x0f00, 0x00f0, 0x000f, 0xf000);
	if(!surf)
		die("Couldn't set window icon");

	SDL_SetWindowIcon(window, surf);
	SDL_FreeSurface(surf);

	sdl_errdie("Couldn't set window icon");
}
